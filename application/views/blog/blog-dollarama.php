                            <div class="single-blog-desc">
                                <p>Alors que le prix d’un panier type d’épicerie a grimpé de près de 12 % en deux ans, Le Journal a constaté que les consommateurs peuvent économiser de 32 % à 45 % s’ils font une partie de leur épicerie chez... Dollarama. Sur une liste de 11 articles, identiques ou équivalents selon le commerce, la facture totale varie entre 23,50 $ chez Dollarama, et 43,19 $ chez Provigo. Si plusieurs consommateurs croient qu’il n’y a que du «cannage» et des bonbons au Dollarama, détrompez-vous. De nombreux produits de marque, également offerts dans les supermarchés, s’y trouvent à des prix jusqu’à deux fois moins élevés. Il y a notamment du beurre d’arachides naturel, de l’édulcorant à base de feuilles de stévia et du gruau de marque connue.</p>

                                <blockquote>Offre complémentaire</blockquote>
								
								<p>Mélissa Boivin, une jeune maman de Lévis, est une habituée du Dollarama et y effectue une partie de son épicerie. «J’achète beaucoup de produits non périssables. Les sauces et les épices ne sont vraiment pas chères», mentionne-t-elle. Elle y achète également, à prix moindres, des produits pour la maison, tels que divers nettoyants. Ce sont également des marques tenues dans d’autres supermarchés. «Dollarama ne se positionne pas comme un détaillant d’alimentation. L’entreprise considère les produits alimentaires comme complémentaires et n’a pas l’intention d’étendre son offre», indique toutefois Alex-Sandra Thibault, relationniste chez National et attitrée au dossier. Elle a ajouté que la raison pour laquelle la bannière pouvait offrir de si bons prix est notamment parce qu’elle a de très bonnes relations avec ses fournisseurs.</p>
								
								<blockquote>L’épicerie écope</blockquote>
								
								<p>Consultante budgétaire à l’ACEF de Québec, Francine Hamel souligne que l’épicerie est souvent le poste budgétaire qui écope en premier quand les ménages se serrent la ceinture. Elle compte suggérer à ses clients d’inclure un arrêt au Dollarama. Les économies possibles sont considérables au bout d’une année, croit Mme Hamel. «Ça peut servir pour rembourser les cartes de crédit», observe-t-elle. Pour économiser, la consultante budgétaire propose également de calculer son panier au fur et à mesure qu’on le remplit ou de ne faire qu’une épicerie hebdomadaire.</p>
								
								<p>Maxi, Walmart et le Tigre Géant proposent également une politique des plus bas prix. «La politique vise les grands supermarchés d’alimentation, sur présence d’une circulaire», précise Johanne Héroux, directrice aux affaires de l’entreprise et aux communications chez Loblaws. «Le Dollarama ne fait pas partie des grands marchés d’alimentation, les pharmacies non plus», précise-t-elle. Interrogée à savoir pourquoi Provigo avait la facture la plus chère, Geneviève Poirier, de chez Loblaws, n’a pas souhaité commenter, «puisque nous ne connaissons pas tous les paramètres de l’étude». Walmart, Maxi et le Tigre Géant sont les seuls à garantir le plus bas prix. Ils vont «accoter» le prix de n’importe quelle épicerie dans la région. Il faut vraiment que tu aies une preuve. Ça prend une circulaire. Ça va être plus difficile de défier les prix du Dollarama, car ils n’ont pas de circulaire.</p>
								
								<p>À part l’application des bons de réduction dans les commerces qui ont la politique des plus bas prix, <blockquote>NOTRE ASTUCE POUR ÉCONOMISER EST D’UTILISER L’ÉPICERIE À PETIT PRIX.</blockquote></p>
								

